def get_mindmap(process: str) -> list:
    """
    返回指定工艺的所有节点列表，供前端 renderMindmap() 使用。
    未建设的工艺返回空列表，前端渲染"建设中"占位。
    """
    return _MINDMAPS.get(process, [])


def get_node(process: str, node_id: str) -> dict | None:
    """
    按 node_id 查找单个节点的完整详情，供详情面板渲染。
    """
    for node in _MINDMAPS.get(process, []):
        if node.get("id") == node_id:
            return node
    return None